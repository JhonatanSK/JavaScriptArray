function soma(numA, numB){
    return numA + numB
}
function subtracao(numA, numB){
    return numA - numB
}
function multiplicacao(numA, numB){
    return numA * numB
}
function divisao(numA, numB){
    return numA / numB
}
console.log(divisao(10,5))
console.log(multiplicacao(10, 3))
console.log(soma(2,8))
